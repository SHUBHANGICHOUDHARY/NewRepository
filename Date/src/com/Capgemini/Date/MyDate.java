package com.Capgemini.Date;

public class MyDate
{
	private int date;
	private int month;
	private int year;
	
	public MyDate(int date, int month, int year) 
	{
		super();
		this.date = date;
		this.month = month;
		this.year = year;
	}
	
	public int getDate() 
	{
		return date;
	}
	
	public int getMonth() 
	{
		return month;
	}
	
	public int getYear() 
	{
		return year;
	}
	
}
